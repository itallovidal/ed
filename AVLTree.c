// Árvores binárias AVL são árvores que possuem
// um fator de balanceamento (FB). É uma propriedade
// que faz com que a árvore se mantenha balanceada, ou
// seja, tenha uma altura de no máximo 1, 0 ou -1.

// Caso a diferença entre as alturas da subárvore esquerda e direita
// seja maior que 1 ou menor que -1, a árvore está desbalanceada
// e precisa fazer alguma rotacão.

// Existem 4 rotacões:

// -> Rotacão Esquerda-Direita (Rotacão dupla)
// -> Rotacão Esquerda-Esquerda  (Rotacão simples)
// -> Rotacão Direita-Esquerda (Rotacão simples)
// -> Rotacão Direita-Direita (Rotacão dupla)